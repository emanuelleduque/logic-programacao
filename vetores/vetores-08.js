// Organização de Fila: Prioridade no Início

// Um vetor com nomes: ["Maria", "João"] 
let nomes = ["Maria", "João"];
console.log("Fila Normal: " + nomes);

// Utilizar .unshift() para adicionar elementos no início do vetor, e o comando para adicionar "Ana" no início.
nomes.unshift("Ana");
console.log("Fila com Prioridade: " + nomes);