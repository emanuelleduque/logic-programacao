//  Gerenciamento de Lista: Compras

// Um vetor vazio [] e comandos para adicionar "Arroz", "Feijão" e "Leite".
let comidas = [];

// Aprender a utilizar métodos dinâmicos para inserir (push) e remover (shift) elementos.
comidas.push('Arroz');
comidas.push(' Feijão');
comidas.push(' Leite');

console.log("Antiga Lista: " + comidas);

comidas.shift();
console.log("Nova Lista: " + comidas);