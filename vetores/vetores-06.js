// Filtragem: Verificação de Maioridade

// Um vetor de idades: [12, 18, 25, 10, 30, 15].
let idades = [12, 18, 25, 10, 30, 15];

// Aplicar lógica condicional ou métodos de alta ordem (filter) para extrair subconjuntos de dados.
let maiorDeIdade = idades.filter(idade => idade >= 18);
console.log("Pessoas maiores de idade: " + maiorDeIdade);
